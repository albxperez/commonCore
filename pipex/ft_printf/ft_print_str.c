#include "ft_printf.h"

int	ft_print_str(char *s)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			i++;
		}
	}
	else if (s == NULL)
	{
		write (1, "(null)", 6);
		return (6);
	}
	return (i);
}
