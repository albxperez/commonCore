int	max(int *tab, unsigned int len)
{
	if(len == '\0')
		return 1;
	
	int i = 0;
	int temp = tab[i++];
	while(i < len)
	{
		if(temp < tab[i])
			temp = tab[i];
		i++;
	}
	return temp;

}
